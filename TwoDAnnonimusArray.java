package New.folder;

public class TwoDAnnonimusArray {
    
    static void sum(int [][] no){
        int total=0;
        for(int ii[]:no){
          for(int i:ii){
            
            total=total+i;
            }
        }
    }
    public static void main(String[] args) {
        TwoDAnnonimusArray.sum(new int[][]{{10},{20}});
    }
    
}

